const zipOf = a => { const m=(a||'').match(/\b(1\d{4})\b/); return m?m[1]:null }
const dist = (a,b,c,d)=>{const R=6371,toR=x=>x*Math.PI/180;const dLat=toR(c-a),dLng=toR(d-b);const s=Math.sin(dLat/2)**2+Math.cos(toR(a))*Math.cos(toR(c))*Math.sin(dLng/2)**2;return 2*R*Math.asin(Math.sqrt(s))}
let gross=[]
for (const p of seedUserPlaces) {
  const zip=zipOf(p.address); if(!zip||!Z[zip]) continue
  if(typeof p.lat!=='number'||typeof p.lng!=='number') continue
  const [zlat,zlng]=Z[zip]; const km=dist(p.lat,p.lng,zlat,zlng)
  if(km>2.0) gross.push({name:p.name,id:p.id,zip,km:+km.toFixed(1),lat:p.lat,lng:p.lng,zc:[zlat,zlng],addr:p.address})
}
gross.sort((a,b)=>b.km-a.km)
console.log('GROSS geocode errors (>2km from ZIP):', gross.length)
for(const g of gross) console.log(`  ${g.km}km | ${g.name} [${g.id}] | ${g.zip} | have ${g.lat},${g.lng} | addr ${g.addr}`)
